func newTestServer(t *testing.T, bank db.Bank) *Server {
	config := util.Config{
		TokenSymmetricKey:   util.RandomString(32),
		AccessTokenDuration: time.Minute,
	}

	server, err := NewServer(config, bank)
	require.NoError(t, err)

	return server
}

